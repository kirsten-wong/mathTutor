    // this method calculates the volume of a rectangular prism
    public  int VOfRectPrism(int length, int width, int height){ // set parameters for length, width, and height
        int answer = length * width * height; // declared variable for answer and assigned value of the mathematical calculation for the area of a rectangular prism
        return (answer); // returns the volume
    }
    
    // this method calculates the area of a circle
    public  double AOfCircle(int radius){ // set parameter for the radius
        double answer = Math.PI * Math.pow(radius, 2); // declared variable for the answer and performed mathmatical calculation for the area of a circle using math methods
        return (answer); // returns the area
        
    }
    
    // this method calculates the area of a rectangle
    public int AOfRectangle (int length, int width){ // set parameters for length and width
        int answer = length * width; // declared variable for answr and assigned value of the mathematical calculation for the area of a rectangle
        return (answer); // returns the area
        
    }
    
    // this method calculates the area of a triangle
    public int AOfTriangle (int base, int height){ // set parameter for base and height
        int answer = (base * height) / 2; // declared variable for answer and assigned it the value of the mathematical calculation for the area of a triangle
        return(answer); // returns the area
    }
    
    // this method calculates the volume of cylinder
    public double VOfCylnd (int radius, int height){ // set parameter for the radius and height
        double answer = Math.PI * Math.pow(radius, 2) * height; // declared variable for answer and assigned it the value of the mathematical calculation for the volume of a cylinder using math methods
        return (answer); // returns the volume 
    }
    
    
