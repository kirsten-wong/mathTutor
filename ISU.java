    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // retrieves the user input measurement and converts it to an integer
        String strMark = this.txtMark.getText();
        int mark = Integer.parseInt(strMark);
        
        // set up if statements and conditions for the user input. When the user enters the number of questions they got correct, a message displaying their percentage grade and letter grade will display
        if (mark == 0){
            this.lblMark.setText("You got a zero. That's a fail.");
        }
        
        else if (mark == 1){
            this.lblMark.setText("You got 10% correct. That's a fail");
        }
        
        else if (mark == 2){
            this.lblMark.setText("You got 20% correct. That's a fail");
        }
        
        else if (mark == 3){
            this.lblMark.setText("You got 30% correct. That's a fail");
        }
        
        else if (mark == 4){
            this.lblMark.setText("You got 40% correct. That's a fail");
        }
        
        else if (mark == 5){
            this.lblMark.setText("You got 50% correct. That's a pass or a D");
        }
        
        else if (mark == 6){
            this.lblMark.setText("You got 60% correct. That's a C");
        }
        
        else if (mark == 7){
            this.lblMark.setText("You got 70% correct. That's a B");
        }
        
        else if (mark == 8){
            this.lblMark.setText("You got 80% correct. That's an A");
        }
        
        else if (mark == 9){
            this.lblMark.setText("You got 10% correct. That's an A+");
        }
        
        else if (mark == 10){
            this.lblMark.setText("You got 100% correct. That's perfect!");
        }
        
        else {
           this.lblMark.setText("That is not an appropriate input"); // error message will display for the user if an inappropriate input is used
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

